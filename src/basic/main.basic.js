const calculateCartAndUpdateUI = () => {
  // Ï¥àÍ∏∞Ìôî
  [totalPrice, totalQuantity] = [0, 0];

  // Ïû•Î∞îÍµ¨Îãà Ï¥ùÏï° Í≥ÑÏÇ∞
  const {
    subTotal,
    totalQuantity: cartTotalQuantity,
    itemDiscounts,
    totalDiscountedPrice,
  } = calculateCartTotals();
  totalQuantity = cartTotalQuantity;

  // ÎåÄÎüâÍµ¨Îß§ Ìï†Ïù∏ Ï†ÅÏö© (Í∞úÎ≥Ñ ÏÉÅÌíà Ìï†Ïù∏Ïù¥ Ï†ÅÏö©Îêú Í∞ÄÍ≤©Ïóê ÎåÄÌï¥)
  const { finalPrice: bulkDiscountedPrice } = applyBulkDiscount(
    totalDiscountedPrice,
    totalQuantity
  );

  // ÌôîÏöîÏùº ÌäπÎ≥Ñ Ìï†Ïù∏ Ï†ÅÏö©
  const { finalPrice, totalDiscountRate, isTuesday } = applyTuesdayDiscount(
    bulkDiscountedPrice,
    subTotal
  );
  totalPrice = finalPrice;

  // Ï¥ù Ìï†Ïù∏Ïú® Í≥ÑÏÇ∞ (ÏõêÍ∞Ä ÎåÄÎπÑ ÏµúÏ¢Ö Í∞ÄÍ≤©Ïùò Ìï†Ïù∏Ïú®)
  const totalDiscountRateCalculated = subTotal > 0 ? (subTotal - totalPrice) / subTotal : 0;

  // UI ÏóÖÎç∞Ïù¥Ìä∏ Ïã§Ìñâ
  updateAllUIComponents(
    cartTotalQuantity,
    subTotal,
    itemDiscounts,
    isTuesday,
    totalDiscountRateCalculated
  );
};

